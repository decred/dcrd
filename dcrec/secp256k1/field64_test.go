// TestField64IsZero ensures that checking if a field is zero via IsZero and
// IsZeroBit works as expected.
func TestField64IsZero(t *testing.T) {
	f := new(FieldVal64)
	if !f.IsZero() {
		t.Errorf("new field value is not zero - got %v (rawints %x)", f, f.n)
	}
	if f.IsZeroBit() != 1 {
		t.Errorf("new field value is not zero - got %v (rawints %x)", f, f.n)
	}

	f.SetInt(1)
	if f.IsZero() {
		t.Errorf("claims zero for nonzero field - got %v (rawints %x)", f, f.n)
	}
	if f.IsZeroBit() == 1 {
		t.Errorf("claims zero for nonzero field - got %v (rawints %x)", f, f.n)
	}

	f.Zero()
	if !f.IsZero() {
		t.Errorf("claims nonzero for zero field - got %v (rawints %x)", f, f.n)
	}
	if f.IsZeroBit() != 1 {
		t.Errorf("claims nonzero for zero field - got %v (rawints %x)", f, f.n)
	}

	f.SetInt(1)
	f.Zero()
	if !f.IsZero() {
		t.Errorf("claims zero for nonzero field - got %v (rawints %x)", f, f.n)
	}
	if f.IsZeroBit() != 1 {
		t.Errorf("claims zero for nonzero field - got %v (rawints %x)", f, f.n)
	}
}

