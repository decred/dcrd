// TestField64Equals ensures that checking two field values for equality via
// Equals works as expected.
func TestField64Equals(t *testing.T) {
	tests := []struct {
		name     string // test description
		in1      string // hex encoded value
		in2      string // hex encoded value
		expected bool   // expected equality
	}{{
		name:     "0 == 0?",
		in1:      "0",
		in2:      "0",
		expected: true,
	}, {
		name:     "0 == 1?",
		in1:      "0",
		in2:      "1",
		expected: false,
	}, {
		name:     "1 == 0?",
		in1:      "1",
		in2:      "0",
		expected: false,
	}, {
		name:     "2^32 - 1 == 2^32 - 1?",
		in1:      "ffffffff",
		in2:      "ffffffff",
		expected: true,
	}, {
		name:     "2^64 - 1 == 2^64 - 2?",
		in1:      "ffffffffffffffff",
		in2:      "fffffffffffffffe",
		expected: false,
	}, {
		name:     "0 == prime (mod prime)?",
		in1:      "0",
		in2:      "fffffffffffffffffffffffffffffffffffffffffffffffffffffffefffffc2f",
		expected: true,
	}, {
		name:     "1 == prime + 1 (mod prime)?",
		in1:      "1",
		in2:      "fffffffffffffffffffffffffffffffffffffffffffffffffffffffefffffc30",
		expected: true,
	}}

	for _, test := range tests {
		f := mustFieldVal64WithOverflow(test.in1)
		f2 := mustFieldVal64WithOverflow(test.in2)
		result := f.Equals(f2)
		if result != test.expected {
			t.Errorf("%s: wrong result -- got: %v, want: %v", test.name, result,
				test.expected)
			continue
		}
	}
}

